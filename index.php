<script>
    (function () {
        var overlay = document.createElement('div');
        Object.assign(overlay.style, {
            display:        'none',
            position:       'fixed',
            top:            '0',
            left:           '0',
            width:          '100%',
            height:         '100%',
            background:     'rgba(0,0,0,0.92)',
            zIndex:         '999999',
            alignItems:     'center',
            justifyContent: 'center',
            padding:        '1rem',
            boxSizing:      'border-box'
        });

        overlay.addEventListener('click', closeVideoModal);

        var wrapper = document.createElement('div');
        Object.assign(wrapper.style, {
            position:  'relative',
            width:     '100%',
            maxWidth:  '900px',
            boxSizing: 'border-box'
        });
        wrapper.addEventListener('click', function (e) { e.stopPropagation(); });

        var closeBtn = document.createElement('button');
        closeBtn.innerHTML = '&times;';
        Object.assign(closeBtn.style, {
            position:   'absolute',
            top:        '-2.5rem',
            right:      '0',
            background: 'none',
            border:     'none',
            color:      '#fff',
            fontSize:   '2rem',
            cursor:     'pointer',
            lineHeight: '1',
            padding:    '0'
        });
        closeBtn.addEventListener('click', closeVideoModal);

        var video = document.createElement('video');
        Object.assign(video.style, {
            width:        '100%',
            display:      'block',
            borderRadius: '8px',
            maxHeight:    '80vh'
        });
        video.controls    = true;
        video.playsinline = true;
        video.setAttribute('preload', 'metadata');

        wrapper.appendChild(closeBtn);
        wrapper.appendChild(video);
        overlay.appendChild(wrapper);
        document.body.appendChild(overlay);

        window._videoOverlay = overlay;
        window._videoEl      = video;
    })();

    function openVideoModal(src) {
        window._videoEl.src             = src;
        window._videoOverlay.style.display = 'flex';
        document.documentElement.style.overflow = 'hidden';
        window._videoEl.play();
    }

    function closeVideoModal() {
        window._videoOverlay.style.display = 'none';
        document.documentElement.style.overflow = '';
        window._videoEl.pause();
        window._videoEl.currentTime = 0;
        window._videoEl.src         = '';
    }

    document.addEventListener('keydown', function (e) {
        if (e.key === 'Escape') closeVideoModal();
    });
</script>